//Existem dois tipos primitivos: referência e de valores

/*Os tipos de referência são: 
    string, number, boolean, undefined, null*/

//String Literal: quando atribuímos um valor com letras que podem ser palavras, frases e também podem conter até números
  let nome = 'Giovana';

//Number Literal: todo o valor que contém números, podem também serem valores decimais
  let idade = 23;

//Boolean: quando queremos que a resposta seja verdadeiro ou falso, assim conseguimos construir algumas regras
  let estaAprovado = true;

//Undefined: tipo primitivo sem valor
  let variavelSemValor; 

/*Null: onde conseguimos redefinir um valor, começa sem valor mas pode ser redefinido.
        Esse é um exemplo onde uma página quando inicializada começa sem cor, 
        pois a pessoa pode selecionar uma cor correspondente ao seu gosto, adicionando um valor, 
        mas no momento em que ela sai da página, o valor retorna null novamente*/
  let corSelecionada = null;